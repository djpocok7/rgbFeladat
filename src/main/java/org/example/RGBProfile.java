package org.example;

public class RGBProfile {

    private Integer red;
    private Integer green;
    private Integer blue;
    public RGBProfile(Integer red, Integer green, Integer blue) {
        this.red = red;
        this.green = green;
        this.blue = blue;
    }
    public Integer getRed() {
        return red;
    }

    public Integer getGreen() {
        return green;
    }

    public Integer getBlue() {
        return blue;
    }
}
